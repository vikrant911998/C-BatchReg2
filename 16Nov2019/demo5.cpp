#include<iostream>
using namespace std;

//Arithmetic operators.
//+,-,*,/,%


int main(){
    int a = 4,b = 15;

    cout<<"Addition = "<<a+b<<endl; //addition operator.
    cout<<"Subtraction = "<<a-b<<endl;//subtraction operator.
    cout<<"Multiplication = "<<a*b<<endl;//multiplication operator.
    cout<<"Division = "<<b/a<<endl;//division operator.
    cout<<"Modulus = "<<b%a<<endl;//modulus operator.


    return 0;
}